public class Asus {
    private String Processor;
    private String Layar;
    private String Camera;
    private String Batre;
    private Integer RAM;
    private String Grafis;
    private String Storage;
    private String OS;
    
    public String getProcessor() {
        return Processor;
    }
    public void setProcessor(String processor) {
        Processor = processor;
    }

    public String getLayar() {
        return Layar;
    }
    public void setLayar(String layar) {
        Layar = layar;
    }

    public String getCamera() {
        return Camera;
    }
    public void setCamera(String camera) {
        Camera = camera;
    }

    public String getBatre() {
        return Batre;
    }
    public void setBatre(String batre) {
        Batre = batre;
    }

    public Integer getRAM() {
        return RAM;
    }
    public void setRAM(Integer rAM) {
        RAM = rAM;
    }

    public String getGrafis() {
        return Grafis;
    }
    public void setGrafis(String grafis) {
        Grafis = grafis;
    }

    public String getStorage() {
        return Storage;
    }
    public void setStorage(String storage) {
        Storage = storage;
    }

    public String getOS() {
        return OS;
    }
    public void setOS(String oS) {
        OS = oS;
    }
    
    
    
}